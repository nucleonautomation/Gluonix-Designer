    from Widget.Slider import Slider
    
    Entry = Root.Main.Entry
    Entry.Set(0)
    Entry.Bind(On_Key_Release = lambda E : Change('Entry'))
    
    Scale = Slider(Root, Root.Main.Bar, Root.Main.Frame)
    Scale.Set(0)
    Scale.Config(Minimum=0, Maximum=100, Increment=1, On_Change = lambda : Change('Scale'))
            
    def Change(Widget):
        Entry = globals()['Entry']
        Scale = globals()['Scale']
        if Widget=='Entry':
            Value = int(Entry.Get())
            Scale.Set(Value)
        else:
            Value = int(Scale.Get())
            Entry.Set(Value)

